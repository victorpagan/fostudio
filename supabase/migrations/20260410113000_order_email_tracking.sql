DROP POLICY IF EXISTS "order_email_messages_staff_read" ON public.order_email_messages;
CREATE POLICY "order_email_messages_staff_read"
  ON public.order_email_messages
  FOR SELECT
  TO authenticated
  USING (public.is_dashboard_staff());

DROP POLICY IF EXISTS "order_email_messages_service_all" ON public.order_email_messages;
CREATE POLICY "order_email_messages_service_all"
  ON public.order_email_messages
  FOR ALL
  TO service_role
  USING (true)
  WITH CHECK (true);

