DROP POLICY IF EXISTS waiver_templates_select_active_or_admin ON public.waiver_templates;
CREATE POLICY waiver_templates_select_active_or_admin
  ON public.waiver_templates
  FOR SELECT
  TO authenticated
  USING (is_active = true OR public.is_admin());

DROP POLICY IF EXISTS waiver_templates_admin_insert ON public.waiver_templates;
CREATE POLICY waiver_templates_admin_insert
  ON public.waiver_templates
  FOR INSERT
  TO authenticated
  WITH CHECK (public.is_admin());

DROP POLICY IF EXISTS waiver_templates_admin_update ON public.waiver_templates;
