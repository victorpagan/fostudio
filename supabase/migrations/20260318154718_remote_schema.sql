CREATE OR REPLACE FUNCTION public.get_secret(secret_name text)
 RETURNS text
 LANGUAGE plpgsql
AS $function$
DECLARE
    decrypted text;
BEGIN
    -- Ensure only authorized roles can execute this function
    IF current_setting('request.jwt.claims', true)::jsonb->>'role' = 'service_role' THEN
        SELECT decrypted_secret
        INTO decrypted
        FROM vault.decrypted_secrets
        WHERE name = secret_name;
        RETURN decrypted;
    ELSE
        RAISE EXCEPTION 'Access denied: only service_role user can execute this function.';
    END IF;
END;
$function$
;

