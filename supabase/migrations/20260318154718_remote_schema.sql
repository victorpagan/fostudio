CREATE OR REPLACE FUNCTION public.queue_print_job_on_order_completed()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
begin
  -- only queue when eligible
  if (
    new."isServiceOrder" = true
    and new."state" = 'COMPLETED'
    and new."locationId" is not null
    and new."customerId" is not null  -- change to your actual "customer attached" signal
  ) then
    insert into print_jobs (order_id, order_square_id, location_id, job_type, status)
