-- Unified activity logging, unread cursors, and staff account linking.
-- Supports dashboard notifications/history from durable DB events.

CREATE TABLE IF NOT EXISTS public.activity_events (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  occurred_at timestamptz NOT NULL DEFAULT now(),
  entity_type text NOT NULL CHECK (entity_type IN ('order', 'ticket', 'customer', 'staff', 'system')),
  entity_id text NOT NULL,
  action text NOT NULL,
  scope text NOT NULL DEFAULT 'all' CHECK (scope IN ('all', 'tickets', 'orders', 'customers')),
  summary text NOT NULL DEFAULT '',
  details jsonb NOT NULL DEFAULT '{}'::jsonb,
  actor_type text NOT NULL DEFAULT 'system' CHECK (actor_type IN ('user', 'system', 'integration')),
  actor_user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  actor_role text,
  actor_service text,
  order_db_id bigint REFERENCES public.orders2(id) ON DELETE SET NULL,
  ticket_id uuid REFERENCES public.internal_errors(id) ON DELETE SET NULL,
  customer_id uuid REFERENCES public.customers(id) ON DELETE SET NULL,
  location_id text,
  dedupe_key text,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE UNIQUE INDEX IF NOT EXISTS activity_events_dedupe_key_unique
  ON public.activity_events (dedupe_key)
  WHERE dedupe_key IS NOT NULL;

CREATE INDEX IF NOT EXISTS activity_events_scope_occurred_idx
  ON public.activity_events (scope, occurred_at DESC);

CREATE INDEX IF NOT EXISTS activity_events_entity_occurred_idx
  ON public.activity_events (entity_type, entity_id, occurred_at DESC);

CREATE INDEX IF NOT EXISTS activity_events_order_occurred_idx
  ON public.activity_events (order_db_id, occurred_at DESC);

CREATE INDEX IF NOT EXISTS activity_events_ticket_occurred_idx
  ON public.activity_events (ticket_id, occurred_at DESC);

CREATE INDEX IF NOT EXISTS activity_events_customer_occurred_idx
  ON public.activity_events (customer_id, occurred_at DESC);

CREATE TABLE IF NOT EXISTS public.activity_read_cursors (
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  scope text NOT NULL CHECK (scope IN ('all', 'tickets', 'orders', 'customers')),
  last_read_at timestamptz NOT NULL DEFAULT 'epoch'::timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (user_id, scope)
);

CREATE INDEX IF NOT EXISTS activity_read_cursors_scope_last_read_idx
  ON public.activity_read_cursors (scope, last_read_at DESC);

CREATE TABLE IF NOT EXISTS public.staff_account_links (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  auth_user_id uuid NOT NULL UNIQUE REFERENCES auth.users(id) ON DELETE CASCADE,
