CREATE OR REPLACE FUNCTION public.trg_order_email_activity_event()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  v_order_id bigint;
  v_entity_id text;
  v_action text;
  v_summary text;
  v_dedupe text;
  v_should_emit boolean := true;
BEGIN
  IF TG_OP = 'UPDATE' THEN
    IF COALESCE(NEW.status, '') = COALESCE(OLD.status, '')
      AND COALESCE(NEW.last_event, '') = COALESCE(OLD.last_event, '')
      AND COALESCE(NEW.last_event_at::text, '') = COALESCE(OLD.last_event_at::text, '')
      AND COALESCE(NEW.open_count, 0) = COALESCE(OLD.open_count, 0) THEN
      v_should_emit := false;
    END IF;
  END IF;

  IF NOT v_should_emit THEN
    RETURN NEW;
  END IF;

  v_order_id := NEW.order_db_id;
  v_entity_id := COALESCE(
    NEW.order_db_id::text,
    NEW.order_number,
    NEW.square_order_id,
    NEW.tracking_id::text,
    NEW.id::text
  );

  v_action := CASE COALESCE(NEW.status, '')
    WHEN 'failed' THEN 'order.email.failed'
    WHEN 'opened' THEN 'order.email.opened'
    WHEN 'delivered' THEN 'order.email.delivered'
    WHEN 'sent' THEN 'order.email.sent'
    WHEN 'queued' THEN 'order.email.queued'
    ELSE 'order.email.updated'
  END;

  v_summary := format(
    'Order email %s %s (%s)',
    COALESCE(NEW.email_kind, 'unknown'),
    COALESCE(NEW.status, 'updated'),
    COALESCE(NEW.recipient_email, 'no-recipient')
  );

  v_dedupe := CASE
    WHEN TG_OP = 'INSERT'
      THEN format('order_email:%s:insert', NEW.id::text)
    ELSE format(
      'order_email:%s:update:%s:%s:%s',
      NEW.id::text,
      COALESCE(NEW.status, 'none'),
      COALESCE(NEW.last_event, 'none'),
      COALESCE(NEW.last_event_at::text, 'none')
    )
  END;

  PERFORM public.record_activity_event(
    'order',
    v_entity_id,
    v_action,
    'orders',
    v_summary,
    jsonb_build_object(
      'message_id', NEW.id,
      'tracking_id', NEW.tracking_id,
      'email_kind', NEW.email_kind,
      'event_type', NEW.event_type,
      'status', NEW.status,
      'sent_at', NEW.sent_at,
      'delivered_at', NEW.delivered_at,
      'opened_first_at', NEW.opened_first_at,
      'opened_last_at', NEW.opened_last_at,
      'open_count', NEW.open_count,
      'last_event', NEW.last_event,
      'last_event_at', NEW.last_event_at,
      'failure_reason', NEW.failure_reason,
      'source_service', NEW.source_service
    ),
    'integration',
    NULL,
    NULL,
    COALESCE(NEW.source_service, 'fomailer'),
    v_order_id,
    NULL,
    NULL,
    NEW.location_id,
    v_dedupe,
    COALESCE(NEW.last_event_at, NEW.created_at, now())
  );

  RETURN NEW;
END;
$$;

