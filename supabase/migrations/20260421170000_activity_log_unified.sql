CREATE OR REPLACE FUNCTION public.resolve_order_db_id(
  p_ss_order_id bigint,
  p_square_order_id text
)
RETURNS bigint
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
  v_order_id bigint;
BEGIN
  IF p_ss_order_id IS NOT NULL THEN
    SELECT o.id
      INTO v_order_id
      FROM public.orders2 o
      WHERE o.id = p_ss_order_id
      LIMIT 1;

    IF v_order_id IS NOT NULL THEN
      RETURN v_order_id;
    END IF;
  END IF;

  IF p_square_order_id IS NOT NULL AND length(btrim(p_square_order_id)) > 0 THEN
    SELECT o.id
      INTO v_order_id
      FROM public.orders2 o
      WHERE o."orderId" = p_square_order_id
      ORDER BY o.created DESC
      LIMIT 1;
  END IF;

  RETURN v_order_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.record_activity_event(
  p_entity_type text,
  p_entity_id text,
  p_action text,
  p_scope text DEFAULT 'all',
  p_summary text DEFAULT '',
  p_details jsonb DEFAULT '{}'::jsonb,
  p_actor_type text DEFAULT 'system',
  p_actor_user_id uuid DEFAULT NULL,
  p_actor_role text DEFAULT NULL,
  p_actor_service text DEFAULT NULL,
  p_order_db_id bigint DEFAULT NULL,
  p_ticket_id uuid DEFAULT NULL,
  p_customer_id uuid DEFAULT NULL,
  p_location_id text DEFAULT NULL,
  p_dedupe_key text DEFAULT NULL,
  p_occurred_at timestamptz DEFAULT NULL
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_id bigint;
BEGIN
  INSERT INTO public.activity_events (
    occurred_at,
    entity_type,
    entity_id,
    action,
    scope,
    summary,
    details,
    actor_type,
    actor_user_id,
    actor_role,
    actor_service,
    order_db_id,
    ticket_id,
    customer_id,
    location_id,
    dedupe_key
  )
  VALUES (
    COALESCE(p_occurred_at, now()),
    p_entity_type,
    p_entity_id,
    p_action,
    p_scope,
    COALESCE(p_summary, ''),
    COALESCE(p_details, '{}'::jsonb),
    COALESCE(p_actor_type, 'system'),
    p_actor_user_id,
    p_actor_role,
    p_actor_service,
    p_order_db_id,
    p_ticket_id,
    p_customer_id,
    p_location_id,
    p_dedupe_key
  )
  ON CONFLICT (dedupe_key) DO NOTHING
  RETURNING id INTO v_id;

  IF v_id IS NOT NULL THEN
    RETURN v_id;
  END IF;

  IF p_dedupe_key IS NOT NULL THEN
    SELECT id INTO v_id
      FROM public.activity_events
      WHERE dedupe_key = p_dedupe_key
      LIMIT 1;
  END IF;

  RETURN v_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.trg_links_activity_event()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  v_order_db_id bigint;
  v_entity_id text;
  v_action text;
  v_summary text;
  v_dedupe text;
BEGIN
  v_order_db_id := public.resolve_order_db_id(NEW."ssOrderId", NEW."orderId");
  v_entity_id := COALESCE(NEW."ssOrderId"::text, NEW."orderId", NEW.id::text);
  v_action := CASE WHEN TG_OP = 'INSERT' THEN 'order.link.created' ELSE 'order.link.updated' END;
  v_summary := CASE
    WHEN TG_OP = 'INSERT' THEN format('Download link created for order %s', v_entity_id)
    ELSE format('Download link updated for order %s', v_entity_id)
  END;
  v_dedupe := format('links:%s:%s:%s', NEW.id::text, lower(TG_OP), coalesce(NEW.expires_date::text, 'none'));

  PERFORM public.record_activity_event(
    'order',
    v_entity_id,
    v_action,
    'orders',
    v_summary,
    jsonb_build_object(
      'source_table', 'links',
      'link_id', NEW.id,
      'ssOrderId', NEW."ssOrderId",
      'orderId', NEW."orderId",
      'expires_date', NEW.expires_date,
      'email', NEW.email,
      'source', NEW.source
    ),
    'system',
    NULL,
    NULL,
    'db_trigger',
    v_order_db_id,
    NULL,
    NULL,
    NEW."locationId",
    v_dedupe,
    now()
  );

  RETURN NEW;
END;
$$;

